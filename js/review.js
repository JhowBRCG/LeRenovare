const prevBtn = document.querySelector(".prev");
const nextBtn = document.querySelector(".next");

const reviewImg = document.querySelector(".review-img");
const nameR = document.querySelector(".name");
const avaliation = document.querySelector(".reviews");
const date = document.querySelector(".date");
const content = document.querySelector(".content");

let currentItem = 0;

window.addEventListener("DOMContentLoaded,", function () {
  showReview(currentItem);
});

function showReview(t) {
  const item = reviews[t];
  reviewImg.src = item.img;
  nameR.textContent = item.personName;
  avaliation.textContent = item.avaliationTimes;
  date.textContent = item.dateReview;
  content.innerHTML = item.contentReview;
}

nextBtn.addEventListener("click", () => {
  currentItem++;
  if (currentItem > reviews.length - 1) {
    currentItem = 0;
  }
  showReview(currentItem);
});

prevBtn.addEventListener("click", () => {
  currentItem--;
  if (currentItem < 0) {
    currentItem = reviews.length - 1;
  }
  showReview(currentItem);
});
